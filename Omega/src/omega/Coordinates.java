package omega;

public class Coordinates {

    int x,y,z;

    public Coordinates(int x,int y,int z){
        this.x=x;
        this.y=y;
        this.z=z;

    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }


    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getZ() {
        return z;
    }
}



